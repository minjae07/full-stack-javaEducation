package kr.or.shi.multitype;

public class Tv {
	
	private int year;
	private int month;
	
	public Tv() {
		
	}

	public Tv(int year, int month) {
		super();
		this.year = year;
		this.month = month;
	}
	

	public int getYear() {
		return year;
	}

	public int getMonth() {
		return month;
	}

	
	
}
